package doubleloop;

public class Loop4 {

	public static void main(String[] args) {
		  	for(int i=1;i<4;i++){
		    for(int j=0;j<i;j++){
		    	int k=0;
		    	k=k+i;
		    	System.out.print(+k);                                        
		      }
		    
		System.out.println();
	}
}
}
		







